    public void carregarDadosNaJTable(ResultSet rs){
        
        try{
            String colunas []= {"id", "Cliente", "Data de Entrega", "Situação", "Valor Total" };
            DefaultTableModel modelo = new DefaultTableModel(null,colunas);
            tbOServ.setModel (modelo);
            
            while (rs.next()){
                String linha[]= {rs.getString("idOrdemDeServico"), rs.getString("nome"), rs.getString("dtEntrega"), rs.getString("situacao"), rs.getString("valorTotal")};
                modelo.addRow(linha);    
            }
        }catch(Exception e){
            System.out.println("Erro ao Carregar JTable");
            System.out.println(e.getMessage());
        }
    }
    
 
