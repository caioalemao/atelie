    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
    
        try {
            OrdemDeServicoDao cDao = new OrdemDeServicoDao();
            OrdemDeServico ods = new OrdemDeServico();
            int linhaSelecionada=tbOServ.getSelectedRow();
            ods.setId(Integer.parseInt(tbOServ.getModel().getValueAt(linhaSelecionada, 0).toString()));
            System.out.println(ods.getId());
            ResultSet rs = cDao.selecionarOrdem(ods);
            
            
            while (rs.next()){
                int id = rs.getInt(1);               
                String dataE = rs.getString(3);
                String situacao = rs.getString(5);
                
                AlterarOrdemDeServicoVIEW tela = new AlterarOrdemDeServicoVIEW();
                tela.setVisible(true);
                tela.setResizable(false);
                tela.recebeCliente(id, dataE, situacao);
                tela.setVisible(true);
                tela.setResizable(false);
            }} catch (SQLException ex) {
            System.out.println(ex.getMessage());;
        }
        btExcluir.setEnabled(false);
        btAlterar.setEnabled(false);
         
    }//GEN-LAST:event_btAlterarActionPerformed

