    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        
        
        OrdemDeServicoDao odsDao= new OrdemDeServicoDao();
        OrdemDeServico ods = new OrdemDeServico();
        int linhaSelecionada = tbOServ.getSelectedRow();
        int codigo = Integer.parseInt(tbOServ.getModel().getValueAt(linhaSelecionada, 0).toString());
        
        ods.setId(codigo);
        odsDao.excluirODS(ods);
        
        btExcluir.setEnabled(false);
        btAlterar.setEnabled(false);
        ResultSet rs= odsDao.pegarDadosParaTabela();
        carregarDadosNaJTable(rs);
        
        
    }//GEN-LAST:event_btExcluirActionPerformed

